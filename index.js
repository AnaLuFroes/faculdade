let valor1 = 2
let valor2 = 10
let operador = '*'

if (operador === '+'){
    console.log ('Soma: ', valor1 + valor2)
}else if (operador === '-'){
    console.log ('Subtração: ', valor1 - valor2)
}else if (operador === '*'){
    console.log('Multiplicação: ', valor1 * valor2)
}else if (operador === '/'){
    console.log('Dividir: ', valor1 / valor2)
}